const generateExpressAPI = async (dir, name) => {
  const packageJson = {
    name,
    version: "1.0.0",
    type: "module",
    scripts: {
      start: "node src/index.js",
      dev: "node --watch src/index.js",
    },
    dependencies: {
      express: "^4.19.2",
      cors: "^2.8.5",
      dotenv: "^16.4.5",
    },
  };

  await fs.writeFile(
    path.join(dir, "package.json"),
    JSON.stringify(packageJson, null, 2),
  );

  const srcDir = path.join(dir, "src");
  await fs.mkdir(srcDir, { recursive: true });

  await fs.writeFile(
    path.join(srcDir, "index.js"),
    `import express from "express";
import cors from "cors";
import dotenv from "dotenv";

dotenv.config();

const app = express();
const PORT = process.env.PORT || 3000;

app.use(cors());
app.use(express.json());

app.get("/", (req, res) => {
  res.json({ message: "Welcome to ${name} API!" });
});

app.listen(PORT, () => {
  console.log(\`Server is running on http://localhost:\${PORT}\`);
});
`,
  );

  await fs.writeFile(path.join(dir, ".env"), `PORT=3000\n`);
};

const generateNodeCLI = async (dir, name) => {
  const packageJson = {
    name,
    version: "1.0.0",
    type: "module",
    bin: {
      [name]: "./bin/index.js",
    },
    scripts: {
      start: "node bin/index.js",
    },
    dependencies: {
      commander: "^12.0.0",
      chalk: "^5.3.0",
      inquirer: "^9.2.15",
    },
  };

  await fs.writeFile(
    path.join(dir, "package.json"),
    JSON.stringify(packageJson, null, 2),
  );

  const binDir = path.join(dir, "bin");
  await fs.mkdir(binDir, { recursive: true });

  const assetsDir = path.join(dir, "assets");
  await fs.mkdir(assetsDir, { recursive: true });

  // Crear la base de src y sus subdirectorios
  const srcDir = path.join(dir, "src");
  await fs.mkdir(path.join(srcDir, "commands"), { recursive: true });
  await fs.mkdir(path.join(srcDir, "ui"), { recursive: true });
  await fs.mkdir(path.join(srcDir, "data"), { recursive: true });
  await fs.mkdir(path.join(srcDir, "utils"), { recursive: true });

  await fs.writeFile(
    path.join(binDir, "index.js"),
    `#!/usr/bin/env node

import { program } from "commander";
import chalk from "chalk";

program
  .name("${name}")
  .description("CLI generated by Dev-CLI")
  .version("1.0.0");

program
  .command("hello")
  .description("Say hello")
  .action(() => {
    console.log(chalk.green("Hello from ${name} CLI!"));
  });

program.parse(process.argv);
`,
  );
};
